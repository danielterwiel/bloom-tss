const CardHeader = React.forwardRef<HTMLDivElement, CardHeaderProps>(
  ({ className, ...props }, ref) => (
    <div ref={ref} className={cn("flex flex-col gap-1.5 p-card", className)} {...props} />
  ),
);
CardHeader.displayName = "CardHeader";

